  const SUPPORTED = ['en','no','sv','da','fi','de'];

  // ---- Detection ----
  function detectLang() {
    // 1. Saved choice wins
    try {
      const saved = localStorage.getItem('builtly-lang');
      if (saved && SUPPORTED.indexOf(saved) >= 0) return saved;
    } catch(e){}

    // 2. Browser languages (navigator.languages is ordered preference)
    const browserLangs = navigator.languages || [navigator.language || 'en'];
    for (let i = 0; i < browserLangs.length; i++) {
      const tag = String(browserLangs[i]).toLowerCase();
      // Map BCP47 tags to our codes
      if (tag.startsWith('nb') || tag.startsWith('nn') || tag.startsWith('no')) return 'no';
      if (tag.startsWith('sv')) return 'sv';
      if (tag.startsWith('da')) return 'da';
      if (tag.startsWith('fi')) return 'fi';
      if (tag.startsWith('de')) return 'de';
      if (tag.startsWith('en')) return 'en';
    }
    return 'en';
  }

  // ---- Apply translations to DOM ----
  function tt(lang, key) {
    return (T[lang] && T[lang][key]) || T.en[key] || '';
  }

  function applyLang(lang) {
    if (SUPPORTED.indexOf(lang) < 0) lang = 'en';
    document.documentElement.lang = lang;

    // Text content
    document.querySelectorAll('[data-i18n]').forEach(function(el){
      const key = el.getAttribute('data-i18n');
      const v = tt(lang, key);
      if (v) el.textContent = v;
    });
    // HTML content (when translation contains inline tags)
    document.querySelectorAll('[data-i18n-html]').forEach(function(el){
      const key = el.getAttribute('data-i18n-html');
      const v = tt(lang, key);
      if (v) el.innerHTML = v;
    });
    // Attributes — data-i18n-attr-{name}="key" sets attribute {name}
    document.querySelectorAll('*').forEach(function(el){
      const attrs = el.attributes;
      for (let i = 0; i < attrs.length; i++) {
        const a = attrs[i];
        if (a.name.indexOf('data-i18n-attr-') === 0) {
          const attrName = a.name.substring('data-i18n-attr-'.length);
          const v = tt(lang, a.value);
          if (v) el.setAttribute(attrName, v);
        }
      }
    });

    // Update picker UI
    const picker = document.getElementById('langPicker');
    const menu = document.getElementById('langMenu');
    if (picker) {
      const codeEl = picker.querySelector('.lang-code');
      if (codeEl) codeEl.textContent = lang.toUpperCase();
    }
    if (menu) {
      menu.querySelectorAll('.lang-option').forEach(function(o){
        o.classList.toggle('active', o.dataset.lang === lang);
      });
    }
  }

