  const SUPPORTED = ['en','no','sv','da','fi','de'];

  // ---- Detection ----
  function detectLang() {
    // 1. Saved choice wins
    try {
      const saved = localStorage.getItem('builtly-lang');
      if (saved && SUPPORTED.indexOf(saved) >= 0) return saved;
    } catch(e){}

    // 2. Browser languages (navigator.languages is ordered preference)
    const browserLangs = navigator.languages || [navigator.language || 'en'];
    for (let i = 0; i < browserLangs.length; i++) {
      const tag = String(browserLangs[i]).toLowerCase();
      // Map BCP47 tags to our codes
      if (tag.startsWith('nb') || tag.startsWith('nn') || tag.startsWith('no')) return 'no';
      if (tag.startsWith('sv')) return 'sv';
      if (tag.startsWith('da')) return 'da';
      if (tag.startsWith('fi')) return 'fi';
      if (tag.startsWith('de')) return 'de';
      if (tag.startsWith('en')) return 'en';
    }
    return 'en';
  }

  // ---- Apply translations to DOM ----
  function tt(lang, key) {
    return (T[lang] && T[lang][key]) || T.en[key] || '';
  }

  function applyLang(lang) {
    if (SUPPORTED.indexOf(lang) < 0) lang = 'en';
    document.documentElement.lang = lang;

    // Text content
    document.querySelectorAll('[data-i18n]').forEach(function(el){
      const key = el.getAttribute('data-i18n');
      const v = tt(lang, key);
      if (v) el.textContent = v;
    });
    // HTML content (when translation contains inline tags)
    document.querySelectorAll('[data-i18n-html]').forEach(function(el){
      const key = el.getAttribute('data-i18n-html');
      const v = tt(lang, key);
      if (v) el.innerHTML = v;
    });
    // Attributes — data-i18n-attr-{name}="key" sets attribute {name}
    document.querySelectorAll('*').forEach(function(el){
      const attrs = el.attributes;
      for (let i = 0; i < attrs.length; i++) {
        const a = attrs[i];
        if (a.name.indexOf('data-i18n-attr-') === 0) {
          const attrName = a.name.substring('data-i18n-attr-'.length);
          const v = tt(lang, a.value);
          if (v) el.setAttribute(attrName, v);
        }
      }
    });

    // Update picker UI
    const picker = document.getElementById('langPicker');
    const menu = document.getElementById('langMenu');
    if (picker) {
      const codeEl = picker.querySelector('.lang-code');
      if (codeEl) codeEl.textContent = lang.toUpperCase();
    }
    if (menu) {
      menu.querySelectorAll('.lang-option').forEach(function(o){
        o.classList.toggle('active', o.dataset.lang === lang);
      });
    }
  }

  // ---- Cookie consent banner ----
  function setupConsent(lang) {
    if (document.getElementById('cookieBanner')) return;
    try {
      if (localStorage.getItem('builtly-consent') === '1') return;
    } catch(e){}

    const banner = document.createElement('div');
    banner.id = 'cookieBanner';
    banner.className = 'cookie-banner';
    banner.setAttribute('role','dialog');
    banner.setAttribute('aria-live','polite');
    banner.innerHTML = ''
      + '<div class="cookie-inner">'
      +   '<p class="cookie-msg" data-i18n="cookie.msg"></p>'
      +   '<div class="cookie-actions">'
      +     '<a href="/trust.html" class="cookie-link" data-i18n="cookie.privacyLink"></a>'
      +     '<button type="button" class="cookie-accept" data-i18n="cookie.accept"></button>'
      +   '</div>'
      + '</div>';
    document.body.appendChild(banner);

    // Apply text now
    banner.querySelectorAll('[data-i18n]').forEach(function(el){
      const key = el.getAttribute('data-i18n');
      el.textContent = tt(document.documentElement.lang || 'en', key);
    });

    // Animate in after a tick
    requestAnimationFrame(function(){
      requestAnimationFrame(function(){ banner.classList.add('show'); });
    });

    banner.querySelector('.cookie-accept').addEventListener('click', function(){
      try { localStorage.setItem('builtly-consent','1'); } catch(e){}
      banner.classList.remove('show');
      setTimeout(function(){ banner.parentNode && banner.parentNode.removeChild(banner); }, 350);
    });
  }

  // ---- Language picker wiring ----
  function wirePicker() {
    const picker = document.getElementById('langPicker');
    const menu = document.getElementById('langMenu');
    if (!picker || !menu) return;

    picker.addEventListener('click', function(e){
      e.stopPropagation();
      const open = picker.getAttribute('aria-expanded') === 'true';
      picker.setAttribute('aria-expanded', String(!open));
      if (open) menu.setAttribute('hidden',''); else menu.removeAttribute('hidden');
    });

    document.addEventListener('click', function(){
      picker.setAttribute('aria-expanded','false');
      menu.setAttribute('hidden','');
    });

    menu.addEventListener('click', function(e){
      e.stopPropagation();
      const opt = e.target.closest('.lang-option');
      if (!opt) return;
      const lang = opt.dataset.lang;
      try { localStorage.setItem('builtly-lang', lang); } catch(e){}
      applyLang(lang);
      picker.setAttribute('aria-expanded','false');
      menu.setAttribute('hidden','');
    });
  }

  // ---- Init ----
  const initial = detectLang();
  applyLang(initial);
  wirePicker();
  // Defer cookie banner slightly so it doesn't compete with first paint
  setTimeout(function(){ setupConsent(initial); }, 600);

  // Expose for debugging
  window.BUILTLY_I18N = { applyLang: applyLang, detect: detectLang, T: T };
